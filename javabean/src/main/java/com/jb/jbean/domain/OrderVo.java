package com.jb.jbean.domain;

public class OrderVo {
	private int oidx;
	private long oid;
	private String oday;
	private int ocnt;
	private int oprice;
	private String o_yx;
	private String pname;
	private int midx;
	private int proidx;
	
	
	public int getOidx() {
		return oidx;
	}
	public void setOidx(int oidx) {
		this.oidx = oidx;
	}
	public long getOid() {
		return oid;
	}
	public void setOid(long oid) {
		this.oid = oid;
	}
	public String getOday() {
		return oday;
	}
	public void setOday(String oday) {
		this.oday = oday;
	}
	public int getOcnt() {
		return ocnt;
	}
	public void setOcnt(int ocnt) {
		this.ocnt = ocnt;
	}
	public int getOprice() {
		return oprice;
	}
	public void setOprice(int oprice) {
		this.oprice = oprice;
	}
	public String getO_yx() {
		return o_yx;
	}
	public void setO_yx(String o_yx) {
		this.o_yx = o_yx;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public int getMidx() {
		return midx;
	}
	public void setMidx(int midx) {
		this.midx = midx;
	}
	public int getProidx() {
		return proidx;
	}
	public void setProidx(int proidx) {
		this.proidx = proidx;
	}
}
